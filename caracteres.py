#Modificaciones que le puedo realizar a cadenas de texto

nombre = "jefferson"

# Convertir texto a mayusculas

nombre.upper()

# Convertir solo la primera letra a mayuscula

nombre.capitalize()

# Convertir texto a minusculas

nombre.lower()

# Eliminar caracteres vacios al principio o final de la cadena

nombre.strip()

# Remplazar un caracter por otro

nombre.replace("o", "a")

# Mostrar la longitud de un cadena de texto

len(nombre)

# Acceder al indice de una cadena de texto

nombre[2]